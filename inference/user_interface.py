        #modify detected_cam to delete unwanted cameras
        for cam_num, cam_activated in reversed(list(enumerate(self.camera_activated))):
            if not(cam_activated):
                self.input_processes[cam_num].terminate()
                self.input_processes.pop(cam_num)
                self.input_shared_frame_buffers.pop(cam_num)
                self.input_buffer_locks.pop(cam_num)
                self.detected_cam.remove_cam(cam_num)

