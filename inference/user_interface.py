    def row_and_col(self, pos):
        pos = pos-self.main_widget.pos()
        x = pos.x()
        y = pos.y()

        for i in range(self.main_widget.layout.rowCount() + 1):
            if self.main_widget.layout.itemAtPosition(i, 0):
                if y - self.main_widget.layout.itemAtPosition(i, 0).widget().pos().y() < 0:
                    break
            else:
                break

        for j in range(self.main_widget.layout.columnCount() + 1):
            if self.main_widget.layout.itemAtPosition(0, j):
                if x - self.main_widget.layout.itemAtPosition(0, j).widget().pos().x() < 0:
                    break
            else:
                break

        return i - 1, j - 1

    def mousePressEvent(self, event):
        self.press_point = event.position().toPoint()
        self.press_row_col = self.row_and_col(self.press_point)

        modifierPressed = QtWidgets.QApplication.keyboardModifiers()
        if not (modifierPressed & QtCore.Qt.ControlModifier) == QtCore.Qt.ControlModifier:
            if not self.press_row_col in self.position_selected:
                for tuple in self.position_selected:
                    item = self.main_widget.layout.itemAtPosition(tuple[0], tuple[1])
                    if item:
                        item.widget().unselect()

                self.position_selected = []

        if self.press_row_col[0] != -1 and self.press_row_col[1] != -1:
            item = self.main_widget.layout.itemAtPosition(self.press_row_col[0], self.press_row_col[1])
            if item:
                widget = item.widget()
                if widget.is_selected():
                    modifierPressed = QtWidgets.QApplication.keyboardModifiers()
                    if (modifierPressed & QtCore.Qt.ControlModifier) == QtCore.Qt.ControlModifier:
                        widget.unselect()
                        self.position_selected.remove((self.press_row_col[0], self.press_row_col[1]))
                else:
                    widget.select()
                    if not (self.press_row_col[0], self.press_row_col[1]) in self.position_selected:
                        self.position_selected.append((self.press_row_col[0], self.press_row_col[1]))

    def mouseMoveEvent(self, event):
        w = self.press_point.x() - event.position().x()
        h = self.press_point.y() - event.position().y()
        if w > 0:
            x = self.press_point.x() - w
        else:
            x = self.press_point.x()
        if h > 0:
            y = self.press_point.y() - h
        else:
            y = self.press_point.y()
        self.draw_rect = QtCore.QRect(x, y, abs(w), abs(h))
        self.update()

        current_row_col = self.row_and_col(event.position().toPoint())
        if current_row_col[0] == -1:
            current_row_col = (0, current_row_col[1])
        if current_row_col[1] == -1:
            current_row_col = (current_row_col[0], 0)

        dif_row_col = (current_row_col[0] - self.press_row_col[0], current_row_col[1] - self.press_row_col[1])
        step = (np.sign(dif_row_col[0]), np.sign(dif_row_col[1]))
        if step[0] == 0:
            step = (1, step[1])
        if step[1] == 0:
            step = (step[0], 1)

        for tuple in self.tempory_selected:
            self.main_widget.layout.itemAtPosition(tuple[0], tuple[1]).widget().unselect()

        self.tempory_selected = []

        for i in range(self.press_row_col[0], current_row_col[0] + step[0], step[0]):
            for j in range(self.press_row_col[1], current_row_col[1] + step[1], step[1]):
                item = self.main_widget.layout.itemAtPosition(i, j)
                if item:
                    item.widget().select()
                    self.tempory_selected.append((i, j))

    def mouseReleaseEvent(self, event):
        modifierPressed = QtWidgets.QApplication.keyboardModifiers()
        if not (modifierPressed & QtCore.Qt.ControlModifier) == QtCore.Qt.ControlModifier:
            if self.tempory_selected:
                for item in self.tempory_selected:
                    if not (item in self.position_selected):
                        self.position_selected.append(item)
                self.tempory_selected = []
            else:
                for tuple in self.position_selected:
                    item = self.main_widget.layout.itemAtPosition(tuple[0], tuple[1])
                    if item:
                        item.widget().unselect()

                self.position_selected = []

                item = self.main_widget.layout.itemAtPosition(self.press_row_col[0], self.press_row_col[1])
                if item:
                    item.widget().select()
                    if not (self.press_row_col[0], self.press_row_col[1]) in self.position_selected:
                        self.position_selected.append((self.press_row_col[0], self.press_row_col[1]))
        else:
            if self.tempory_selected:
                for item in self.tempory_selected:
                    if not (item in self.position_selected):
                        self.position_selected.append(item)
                self.tempory_selected = []

        self.draw_rect = QtCore.QRect(0, 0, 0, 0)
        self.update()

    def dragEnterEvent(self, event):
        event.accept()

    def dropEvent(self, event):
        card_list_removed = event.source().parent().parent().remove_selection()
        self.add_cards(card_list_removed)
        event.accept()

    def paintEvent(self, event):
        painter = QtGui.QPainter()
        painter.begin(self)
        painter.setPen(QtGui.QPen(QtGui.QColor(0, 0, 255), 0, QtGui.Qt.PenStyle.SolidLine))
        painter.setBrush(QtGui.QBrush(QtGui.QColor(0, 128, 255, 128), QtGui.Qt.BrushStyle.SolidPattern))
        painter.drawRect(self.draw_rect)

