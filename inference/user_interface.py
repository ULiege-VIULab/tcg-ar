STATUS_OPTIONS = ["", "Asleep", "Burned", "Confused", "Paralyzed", "Poisoned"]


class Broadcast_panel(QtWidgets.QWidget):
    """Operator window to enter the broadcast info the system cannot perceive:
    player names/countries/scores, stadium, a side-swap toggle, and each active
    Pokemon's current HP + status. Writes live into the shared ``Broadcast_state``
    that ``Caster_renderer`` reads every frame. The active card name / max HP are
    filled automatically from the live board (refreshed on a timer)."""

    def __init__(self, show_camera_widget):
        super().__init__(show_camera_widget)
        self.setWindowFlag(QtCore.Qt.Window, True)
        self.setWindowTitle("Broadcast control panel")
        self.setWindowIcon(QtGui.QIcon(_icon("Broadcast")))
        self.scw = show_camera_widget
        self.state = show_camera_widget.broadcast_state
        self._last_active = {"left": None, "right": None}

        layout = QtWidgets.QVBoxLayout(self)

        # --- Match ---------------------------------------------------------
        match_box = QtWidgets.QGroupBox("Match")
        match_form = QtWidgets.QFormLayout(match_box)
        self.stadium_edit = QtWidgets.QLineEdit(self.state.stadium)
        self.stadium_edit.textChanged.connect(self._apply)
        self.side_swap_check = QtWidgets.QCheckBox("Swap which side is Player 1")
        self.side_swap_check.setChecked(self.state.side_swap)
        self.side_swap_check.stateChanged.connect(self._apply)
        match_form.addRow("Stadium:", self.stadium_edit)
        match_form.addRow("", self.side_swap_check)
        layout.addWidget(match_box)

        # --- Players + actives (left / right) ------------------------------
        players_row = QtWidgets.QHBoxLayout()
        self.fields = {}
        for side, title in (("left", "Player 1  (left panel)"), ("right", "Player 2  (right panel)")):
            box = QtWidgets.QGroupBox(title)
            form = QtWidgets.QFormLayout(box)
            pinfo = self.state.player1 if side == "left" else self.state.player2

            name = QtWidgets.QLineEdit(pinfo.get("name", ""))
            country = QtWidgets.QLineEdit(pinfo.get("country", ""))
            score = QtWidgets.QSpinBox()
            score.setRange(0, 99)
            score.setValue(int(pinfo.get("score", 0)))
            for w in (name, country):
                w.textChanged.connect(self._apply)
            score.valueChanged.connect(self._apply)

            active_name = QtWidgets.QLabel("—")
            active_name.setStyleSheet("font-weight: bold;")
            hp = QtWidgets.QSpinBox()
            hp.setRange(0, 1000)
            hp.setSingleStep(10)
            hp.valueChanged.connect(self._apply)
            status = QtWidgets.QComboBox()
            status.addItems(STATUS_OPTIONS)
            status.currentTextChanged.connect(self._apply)

            form.addRow("Name:", name)
            form.addRow("Country:", country)
            form.addRow("Score:", score)
            form.addRow(_hline())
            form.addRow("Active:", active_name)
            form.addRow("Current HP:", hp)
            form.addRow("Status:", status)

            self.fields[side] = {"name": name, "country": country, "score": score,
                                 "active_name": active_name, "hp": hp, "status": status}
            players_row.addWidget(box)
        layout.addLayout(players_row)

        hint = QtWidgets.QLabel("Edits update the broadcast stream live. Active card / max HP "
                                "come from the board automatically; a new active resets HP to full.")
        hint.setWordWrap(True)
        hint.setStyleSheet("color: #6b7a90;")
        layout.addWidget(hint)

        self._refresh_actives()
        self._timer = QtCore.QTimer(self)
        self._timer.timeout.connect(self._refresh_actives)
        self._timer.start(500)

    def _apply(self):
        """Push every widget value into the shared Broadcast_state."""
        self.state.stadium = self.stadium_edit.text()
        self.state.side_swap = self.side_swap_check.isChecked()
        for side, pinfo in (("left", self.state.player1), ("right", self.state.player2)):
            f = self.fields[side]
            pinfo["name"] = f["name"].text()
            pinfo["country"] = f["country"].text()
            pinfo["score"] = f["score"].value()
            self.state.active[side]["hp"] = f["hp"].value()
            self.state.active[side]["status"] = f["status"].currentText()

    def _refresh_actives(self):
        """Fill the active-card name / max HP from the live board. When the active
        Pokemon changes, reset the HP spinbox to that card's max (full HP)."""
        game_state = getattr(self.scw, "game_state", None)
        if game_state is None:
            return
        try:
            layout = board_layout(game_state, self.state.side_swap)
        except Exception:
            return
        db = self.scw.card_database
        for side, key in (("left", "player1"), ("right", "player2")):
            f = self.fields[side]
            active = layout[key].get("active")
            if not active:
                f["active_name"].setText("—")
                self._last_active[side] = None
                continue
            card_id = active["card_id"]
            meta = db.get(card_id, {}) if db else {}
            name = meta.get("name") or card_id
            try:
                maximum = int(meta.get("hp")) if meta.get("hp") else 0
            except (TypeError, ValueError):
                maximum = 0
            f["active_name"].setText(f"{name}  (max HP {maximum})" if maximum else str(name))
            if self._last_active[side] != card_id:
                # New active Pokemon -> start at full HP, clear status.
                self._last_active[side] = card_id
                f["hp"].blockSignals(True)
                f["hp"].setValue(maximum)
                f["hp"].blockSignals(False)
                f["status"].setCurrentIndex(0)
                self.state.active[side]["hp"] = maximum
                self.state.active[side]["status"] = ""


