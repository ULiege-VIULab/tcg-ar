#Columns of the Stream production grid, in output-buffer index order:
#index = camera + STREAM_KINDS.index(kind) * nb_cam
STREAM_KINDS = ["Raw", "AR", "Broadcast"]


class Stream_panel(QtWidgets.QWidget):
    """Operator window to choose which output streams are produced. Each checkbox
    maps to one output buffer (camera x {Raw, AR, Broadcast}); ticking it calls
    Show_camera_widget.set_stream_active so production + RTSP sending start/stop for
    that stream. Fewer ticked boxes => less per-frame work => higher framerate. A
    timer re-syncs the boxes so clicking a preview tile and this panel stay in step."""

    def __init__(self, show_camera_widget):
        super().__init__(show_camera_widget)
        self.setWindowFlag(QtCore.Qt.Window, True)
        self.setWindowTitle("Stream production")
        self.setWindowIcon(QtGui.QIcon(_icon("Broadcast")))
        self.scw = show_camera_widget
        self.nb_cam = show_camera_widget.detected_cam.get_nb_cam_available()

        layout = QtWidgets.QVBoxLayout(self)
        intro = QtWidgets.QLabel("Tick the camera shots to produce. Each produced shot "
                                 "adds frame-creation and encoder load, so fewer shots "
                                 "give a higher framerate.")
        intro.setWordWrap(True)
        layout.addWidget(intro)

        grid_box = QtWidgets.QGroupBox("Produced streams")
        grid = QtWidgets.QGridLayout(grid_box)
        grid.addWidget(QtWidgets.QLabel("Camera"), 0, 0)
        for c, kind in enumerate(STREAM_KINDS):
            grid.addWidget(QtWidgets.QLabel(kind), 0, c + 1, QtCore.Qt.AlignmentFlag.AlignCenter)

        self.checks = {}  # index -> QCheckBox
        for cam in range(self.nb_cam):
            grid.addWidget(QtWidgets.QLabel("Camera %d" % cam), cam + 1, 0)
            for c in range(len(STREAM_KINDS)):
                index = cam + c * self.nb_cam
                box = QtWidgets.QCheckBox()
                box.setChecked(self.scw.camera_activated[index])
                box.toggled.connect(lambda checked, i=index: self.scw.set_stream_active(i, checked))
                grid.addWidget(box, cam + 1, c + 1, QtCore.Qt.AlignmentFlag.AlignCenter)
                self.checks[index] = box
        layout.addWidget(grid_box)

        self._timer = QtCore.QTimer(self)
        self._timer.timeout.connect(self._sync)
        self._timer.start(500)

    def _sync(self):
        """Reflect the live activation state (e.g. changed via a preview-tile click)
        without re-triggering set_stream_active."""
        for index, box in self.checks.items():
            active = self.scw.camera_activated[index]
            if box.isChecked() != active:
                box.blockSignals(True)
                box.setChecked(active)
                box.blockSignals(False)


class Card_picture_loaded(QtWidgets.QWidget):
    def __init__(self, parent, pokemon_card_image_path, placeholder):
        super().__init__(parent)
        self.pokemon_card_image_file_path = pokemon_card_image_path
        self.setPicture(placeholder)

    def setPicture(self, picture):
        self.picture = picture
        self.update()

    def sizeHint(self):
        return self.picture.size()

    def paintEvent(self, e):
        painter = QtGui.QPainter(self)
        painter.drawPixmap(0, 0, self.picture)

    def made_image_visible(self):
        pokemon_card_image = cv2.imread(self.pokemon_card_image_file_path)
        size = pokemon_card_image.shape
        height = 100
        pokemon_card_image = cv2.resize(pokemon_card_image, (int(size[1] / size[0] * height), height), interpolation = cv2.INTER_AREA)
        height = pokemon_card_image.shape[0]
        width = pokemon_card_image.shape[1]
        bit_per_lines = 3 * width
        self.picture = QtGui.QImage(pokemon_card_image, width, height, bit_per_lines, QtGui.QImage.Format_RGB888).rgbSwapped()

        self.setPicture(QtGui.QPixmap(self.picture))

        self.setMinimumSize(width, height)

