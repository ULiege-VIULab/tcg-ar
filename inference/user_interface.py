class Deck_scan_worker(QtCore.QThread):
    """Background thread that scans one camera for N frames and returns all card IDs found."""
    cards_found = QtCore.Signal(list)
    status      = QtCore.Signal(str)
    progress    = QtCore.Signal(int)

    def __init__(self, cam_index: int, n_frames: int = 90):
        super().__init__()
        self.cam_index = cam_index
        self.n_frames  = n_frames

    def run(self):
        import sys as _sys
        import traceback as _tb
        try:
            import torch
            from core.models.detection   import DetectionModel
            from core.models.orientation import OrientationModel
            from core.models             import identification as idm
            from core.transforms         import get_inference_transform
            from core                    import config as _cfg

            device    = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
            transform = get_inference_transform(IDENTIFICATION_IMAGE_SIZE)

            print(f"[Deck scan] device={device}", file=_sys.stderr, flush=True)

            self.status.emit("[1/4] Loading detection model…")
            print("[Deck scan] loading detection model", file=_sys.stderr, flush=True)
            det = DetectionModel(); det.load_detection_model(device)

            self.status.emit("[2/4] Loading orientation model…")
            print("[Deck scan] loading orientation model", file=_sys.stderr, flush=True)
            ori = OrientationModel(); ori.load_orientation_model(device)

            self.status.emit("[3/4] Loading ArcFace model (base weights, no fine-tuning)…")
            print("[Deck scan] loading ArcFace model (fine_tuned=False)", file=_sys.stderr, flush=True)
            id_model = idm.load_model(_cfg.IDENTIFICATION_METHOD, fine_tuned=False, device=device)

            anchor_list = idm.all_card_anchor_list()
            self.status.emit(f"[4/4] Computing embeddings for {len(anchor_list)} cards — please wait…")
            print(f"[Deck scan] evaluate_anchors for {len(anchor_list)} cards (num_workers=0)…", file=_sys.stderr, flush=True)
            # num_workers=0: DataLoader multiprocessing workers spawned from a QThread
            # hang on Windows (spawn start method re-imports the whole app).
            anchors = idm.evaluate_anchors(
                id_model, device,
                anchor_list=anchor_list,
                num_workers=0,
                progress_callback=lambda v: self.progress.emit(v),
                model_path=_cfg.IDENTIFICATION_ARCFACE_MODEL_SAVE_PATH)
            print(f"[Deck scan] anchors ready: {len(anchors)}", file=_sys.stderr, flush=True)
            card_id_list = idm.load_card_id_list()

            # ── open camera ──────────────────────────────────────────────────
            self.status.emit(f"Opening camera {self.cam_index}…")
            print(f"[Deck scan] opening camera {self.cam_index}", file=_sys.stderr, flush=True)
            if platform.system() == 'Windows':
                cap = cv2.VideoCapture(self.cam_index, cv2.CAP_MSMF)
                if not cap.isOpened():
                    # Some Windows cameras (e.g. USB via certain drivers) require
                    # DirectShow instead of MSMF.
                    print("[Deck scan] MSMF failed, retrying with DSHOW", file=_sys.stderr, flush=True)
                    cap = cv2.VideoCapture(self.cam_index, cv2.CAP_DSHOW)
            elif platform.system() == 'Linux':
                cap = cv2.VideoCapture(self.cam_index, cv2.CAP_V4L2)
            else:
                cap = cv2.VideoCapture(self.cam_index)

            if not cap.isOpened():
                msg = f"Camera {self.cam_index} failed to open."
                print(f"[Deck scan] ERROR: {msg}", file=_sys.stderr, flush=True)
                self.status.emit(f"ERROR: {msg}")
                self.cards_found.emit([])
                return

            # Force 1080p — matches what the live app requests via webcam_read.
            cap.set(cv2.CAP_PROP_FRAME_WIDTH,  WIDTH)
            cap.set(cv2.CAP_PROP_FRAME_HEIGHT, HEIGHT)

            # Warm-up: discard the first few frames so the camera AGC settles.
            for _ in range(5):
                cap.read()

            actual_w = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
            actual_h = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
            print(f"[Deck scan] camera opened — resolution {actual_w}×{actual_h}", file=_sys.stderr, flush=True)

            # ── scan loop ─────────────────────────────────────────────────────
            seen: set = set()
            frames_ok = frames_with_cards = consecutive_fail = 0

            for frame_idx in range(self.n_frames):
                ok, frame = cap.read()
                if not ok:
                    consecutive_fail += 1
                    if consecutive_fail > 10:
                        print(f"[Deck scan] 10+ consecutive read failures — stopping early at frame {frame_idx}", file=_sys.stderr, flush=True)
                        break
                    continue
                consecutive_fail = 0
                frames_ok += 1

                bboxes = det.detect_card_location(frame)
                cards  = det.extract_cards(frame, bboxes)

                # Log first frame and any frame that has detections (to stderr).
                if frame_idx == 0 or bboxes:
                    print(f"[Deck scan] frame {frame_idx}: shape={frame.shape} bboxes={len(bboxes)}", file=_sys.stderr, flush=True)

                self.status.emit(
                    f"Scanning frame {frame_idx + 1}/{self.n_frames} — "
                    f"{len(bboxes)} bbox(es) — {len(seen)} card(s) identified so far")

                if not cards:
                    continue

                frames_with_cards += 1
                original_cards = []
                orientation_cards = torch.tensor([]).to(device)
                for card in cards:
                    original_cards.append(copy.copy(card))
                    ct = cv2.cvtColor(card, cv2.COLOR_BGR2RGB)
                    ct = transform(ct)
                    orientation_cards = torch.cat((orientation_cards, torch.unsqueeze(ct, 0).to(device)))

                if not orientation_cards.numel():
                    continue

                orient_out = ori.model(orientation_cards).detach().to('cpu').numpy()
                id_cards   = orientation_cards.clone()
                for idx, oo in enumerate(orient_out):
                    if oo[0] > 0:
                        original_cards[idx] = cv2.rotate(original_cards[idx], cv2.ROTATE_180)
                        ct = cv2.cvtColor(original_cards[idx], cv2.COLOR_BGR2RGB)
                        id_cards[idx] = torch.unsqueeze(transform(ct), 0).to(device)

                cards_id, _ = idm.identify_cards(
                    id_cards, anchors, id_model, device, card_id_list,
                    method=_cfg.IDENTIFICATION_METHOD)
                seen.update(str(c) for c in cards_id if c)

            cap.release()
            summary = (f"Scan complete — {frames_ok}/{self.n_frames} frames read, "
                       f"{frames_with_cards} with detections, {len(seen)} unique card(s)")
            print(f"[Deck scan] {summary}", file=_sys.stderr, flush=True)
            self.status.emit(summary)
            self.cards_found.emit(list(seen))

        except Exception as e:
            _tb.print_exc(file=_sys.stderr)
            self.status.emit(f"Scan failed: {e}")
            self.cards_found.emit([])


