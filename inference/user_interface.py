    @staticmethod
    def _read_deck_file(path):
        """Read a deck .txt into a list of card IDs (tolerant of a missing file)."""
        try:
            with open(path, "r") as f:
                return [line.strip() for line in f.read().splitlines() if line.strip()]
        except FileNotFoundError:
            return []

    def _refresh_deck_combos(self):
        """Populate every Load dropdown with the saved deck files in DECK_LIST_FOLDER."""
        files = []
        if os.path.isdir(DECK_LIST_FOLDER):
            files = sorted(f for f in os.listdir(DECK_LIST_FOLDER) if f.endswith(".txt"))
        for combo in self._load_combos:
            current = combo.currentText()
            combo.blockSignals(True)
            combo.clear()
            combo.addItems(files)
            i = combo.findText(current)
            if i >= 0:
                combo.setCurrentIndex(i)
            combo.blockSignals(False)

    def _refresh_database_filter(self):
        """Hide cards from the database panel that are already in both players' decks."""
        both = set(self.deck_p1.get_ID_list()) & set(self.deck_p2.get_ID_list())
        self.card_database.set_committed_exclude(both)

    def add_to_deck(self, deck):
        """Copy the cards selected in the database into ``deck``. The database is not
        depleted and the selection is kept, so the same selection can immediately be
        added to the other deck too; a card already in this deck is skipped."""
        selected = self.card_database.peek_selection()
        existing = set(deck.get_ID_list())
        new = [c for c in selected if c not in existing]
        if new:
            deck.add_cards(new)
        self._refresh_database_filter()

    def save_deck(self, deck, name_edit):
        name = name_edit.text().strip()
        if not name:
            QtWidgets.QMessageBox.information(self, "Save deck", "Enter a deck name first.")
            return
        os.makedirs(DECK_LIST_FOLDER, exist_ok=True)
        with open(DECK_LIST_FOLDER + name + ".txt", "w") as f:
            f.write("\n".join(sorted(set(deck.get_ID_list()))))
        self._refresh_deck_combos()

    def load_deck(self, deck, combo, name_edit):
        fname = combo.currentText()
        if not fname:
            return
        deck.set_cards(self._read_deck_file(DECK_LIST_FOLDER + fname))
        name_edit.setText(fname.removesuffix(".txt"))
        self._refresh_database_filter()

    def search_match_ID(self, search_criterion):
        card_list_to_show = []

        for value in self.card_database_list.items():
            if search_criterion in value[1]["name"].lower() or search_criterion in value[0].lower():
                card_list_to_show.append(value[0])

        return card_list_to_show

    def filter_by_ID_right(self):
        search_criterion = self.edit_right.search_name_text.text().lower()

        self.card_database.filter_ID(self.search_match_ID(search_criterion))

    def search_match_number(self, search_criterion):
        card_list_to_show = []

        if search_criterion == "":
            for value in self.card_database_list.items():
                if search_criterion in value[1]["number"].lower():
                    card_list_to_show.append(value[0])

        else:
            for value in self.card_database_list.items():
                if search_criterion == value[1]["number"].lower():
                    card_list_to_show.append(value[0])

        return card_list_to_show

    def filter_by_number_right(self):
        search_criterion = self.edit_right.search_number_text.text().lower()

        self.card_database.filter_number(self.search_match_number(search_criterion))

    def search_match_set(self, search_criterion):
        card_list_to_show = []

        if search_criterion == "":
            for value in self.card_database_list.items():
                if search_criterion in value[1]["set"]["id"].lower():
                    card_list_to_show.append(value[0])

            return card_list_to_show

        else:
            for value in self.card_database_list.items():
                if search_criterion == value[1]["set"]["id"].lower():
                    card_list_to_show.append(value[0])

            return card_list_to_show

    def filter_by_set_right(self):
        search_criterion = self.edit_right.scroll_list_extension.currentData().lower()

        self.card_database.filter_set(self.search_match_set(search_criterion))

