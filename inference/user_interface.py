class Compute_homography_worker(QtCore.QObject):
    progress = QtCore.Signal(int)
    finished = QtCore.Signal(bool)
    error = QtCore.Signal(bool)
    homography_matrices = QtCore.Signal(list)

    def __init__(self, zenith_camera_id, detected_cam):
        super().__init__()
        self.zenith_camera_id = zenith_camera_id
        self.detected_cam = detected_cam
        self.nb_camera_calibrated = 0
        self.nb_camera_to_calibrate = self.detected_cam.get_nb_cam_available() - 1
        
        self.input_buffer_locks = list()
        self.input_shared_frame_buffers = list()

        for id_cam in self.detected_cam.get_cam_ids():
            self.input_buffer_locks.append(Lock())
            shared_frame_buffer = Shared_frame_buffer(id = id_cam,
                                                    io = 0,
                                                    length = BUFFER_LENGTH,
                                                    resolution = (HEIGHT, WIDTH, CHANNELS),
                                                    existing_shm = True)
            self.input_shared_frame_buffers.append(shared_frame_buffer)

    def run(self):
        frames = list()

        for num_cam_to_calibrate, id_cam_to_calibrate in enumerate(self.detected_cam.get_cam_ids()):
            frame_read = read_a_frame_from_buffer(buffer = self.input_shared_frame_buffers[num_cam_to_calibrate], 
                                                  lock = self.input_buffer_locks[num_cam_to_calibrate])
            
            while isinstance(frame_read, int):
                frame_read = read_a_frame_from_buffer(buffer = self.input_shared_frame_buffers[num_cam_to_calibrate], 
                                                      lock = self.input_buffer_locks[num_cam_to_calibrate])

            if id_cam_to_calibrate != self.zenith_camera_id:
                frames.append(frame_read)
            else:
                frames.append(np.array([None]))
                zenithal_frame = frame_read

        homography_matrices = list()
        for frame in frames:
            if np.any(frame):
                try:
                    homography_matrices.append(compute_homography_matrix(zenithal_frame, frame))
                except:
                    self.error.emit(True)
                    return
                self.nb_camera_calibrated += 1
                self.progress.emit(self.nb_camera_calibrated / self.nb_camera_to_calibrate * 100)
            else:
                homography_matrices.append(np.eye(3,3))
        
        self.homography_matrices.emit(homography_matrices)
        self.progress.emit(100)
        self.finished.emit(True)

