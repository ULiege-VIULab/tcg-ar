        self.zenith_camera_id = zenith_camera_id
        self.homography_matrices = homography_matrices
        #3 times: raw frame, AR frame, and broadcast/caster overlay per camera
        self.nb_row = np.uint(np.rint(np.sqrt(3 * self.detected_cam.get_nb_cam_available())))
        self.nb_column = np.uint(np.ceil(np.sqrt(3 * self.detected_cam.get_nb_cam_available())))
        self.layout = QtWidgets.QGridLayout(self)
        self.input_buffer_locks = locks
        self.input_shared_frame_buffers = buffer
