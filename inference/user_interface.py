    def save_active_deck(self):
        """Write the active deck file that identification / fine-tuning read.
        If 'Use all cards' is checked, writes an empty file so identification
        falls back to the full card database. Otherwise writes the de-duplicated
        union of both player decks. Returns the written card list."""
        os.makedirs(DECK_LIST_FOLDER, exist_ok=True)
        if self.use_all_cards_cb.isChecked():
            open(IDENTIFICATION_FINE_TUNE_DECK_LIST_FILE, 'w').close()
            return []
        union = sorted(set(self.deck_p1.get_ID_list()) | set(self.deck_p2.get_ID_list()))
        with open(IDENTIFICATION_FINE_TUNE_DECK_LIST_FILE, 'w') as deck_list_file:
            deck_list_file.write("\n".join(union))
        return union

    def _on_scan_cam_changed(self):
        """Open the camera selected in the combo box for the live preview."""
        if self._preview_cap is not None:
            self._preview_cap.release()
            self._preview_cap = None
        cam_id = self.scan_cam_combo.currentData()
        if cam_id is None or cam_id < 0:
            return
        if platform.system() == 'Windows':
            self._preview_cap = cv2.VideoCapture(cam_id, cv2.CAP_MSMF)
            if not self._preview_cap.isOpened():
                self._preview_cap = cv2.VideoCapture(cam_id, cv2.CAP_DSHOW)
        elif platform.system() == 'Linux':
            self._preview_cap = cv2.VideoCapture(cam_id, cv2.CAP_V4L2)
        else:
            self._preview_cap = cv2.VideoCapture(cam_id)
        if not self._preview_cap.isOpened():
            self._preview_cap = None

    def _update_scan_preview(self):
        """Timer slot: grab one frame and update the preview label."""
        if self._preview_cap is None or not self._preview_cap.isOpened():
            return
        ok, frame = self._preview_cap.read()
        if not ok:
            return
        frame = cv2.resize(frame, (320, 180))
        rgb = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
        h, w, ch = rgb.shape
        qimg = QtGui.QImage(rgb.tobytes(), w, h, ch * w, QtGui.QImage.Format.Format_RGB888)
        self.scan_preview_label.setPixmap(QtGui.QPixmap.fromImage(qimg))

    def _stop_scan_preview(self):
        """Stop the preview timer and release the capture device."""
        if hasattr(self, '_preview_timer'):
            self._preview_timer.stop()
        if self._preview_cap is not None:
            self._preview_cap.release()
            self._preview_cap = None

    def _start_scan(self):
        cam_id = self.scan_cam_combo.currentData()
        if cam_id is None or cam_id < 0:
            self.scan_status_label.setText("No camera available.")
            return
        self._stop_scan_preview()  # free the camera for the scan worker
        self._scanner = Deck_scan_worker(cam_id)
        self._scanner.status.connect(self.scan_status_label.setText)
        self._scanner.cards_found.connect(self._on_scan_done)
        self._scanner.progress.connect(self.scan_progress.setValue)
        self.scan_progress.setValue(0)
        self.scan_progress.setVisible(True)
        self.scan_btn.setEnabled(False)
        self._scanner.start()

    def _on_scan_done(self, ids):
        self.scan_progress.setVisible(False)
        deck = self.deck_p1 if self.scan_player_combo.currentIndex() == 0 else self.deck_p2
        existing = set(deck.get_ID_list())
        new_ids = [c for c in ids if c not in existing]
        if new_ids:
            deck.add_cards(new_ids)
        # Append the deck-add result to whatever summary the worker already set.
        current = self.scan_status_label.text()
        if not current.startswith("Scan failed"):
            self.scan_status_label.setText(
                current + f"  →  {len(new_ids)} new card(s) added to deck.")
        self.scan_btn.setEnabled(True)
        self._refresh_database_filter()
        # Restart preview after scan completes
        self._on_scan_cam_changed()
        if hasattr(self, '_preview_timer'):
            self._preview_timer.start()

    def _clear_embedding_cache(self):
        import glob
        from core.config import EMBEDDING_CACHE_PATH
        files = glob.glob(EMBEDDING_CACHE_PATH + "*.pt")
        for f in files:
            try:
                os.remove(f)
            except OSError:
                pass
        QtWidgets.QMessageBox.information(
            self, "Embedding cache",
            f"Cache cleared — {len(files)} file(s) deleted.")

    def save_and_continue(self, use_fine_tuned):
        self._stop_scan_preview()  # release camera before the live app takes over
        global SELECTED_USE_FINE_TUNED
        SELECTED_USE_FINE_TUNED = use_fine_tuned

        # Identification (and optional fine-tuning) restrict to the union of both decks.
        self.save_active_deck()

        # Runtime deck change: update the shared flag the running pipe reads on reload.
        if self.main_widget:
            self.main_widget.use_fine_tuned.value = use_fine_tuned

        next_widget = Fine_tune_widget(self.parentWidget(), self.main_widget, use_fine_tuned)
        self.parent().setCentralWidget(next_widget)

