        for cam_num in range(self.detected_cam.get_nb_cam_available()):
            frame_read = read_a_frame_from_buffer(buffer = self.input_shared_frame_buffers[cam_num], 
                                                    lock = self.input_buffer_locks[cam_num])
            #switch to next cam
            if isinstance(frame_read, int):
                continue

            frame_width = np.uint(self.frameSize().width() / self.nb_column)
            frame_height = np.uint(self.frameSize().height() / self.nb_column)
            resized_frame = cv2.resize(frame_read, (frame_width, frame_height), interpolation = cv2.INTER_AREA)

            if self.camera_activated[cam_num]:
                self.button_list[cam_num].update_picture(resized_frame, self.camera_activated[cam_num])
