class Init_worker(QtCore.QObject):
    progress = QtCore.Signal(int)
    result = QtCore.Signal(list)
    finished = QtCore.Signal(bool)

    def __init__(self):
        super().__init__()

    def run(self):
        results = init(self.progress)
        if results == -1:
            self.progress.emit(42)
            self.finished.emit(True)
        else:
            self.result.emit(results)
            self.progress.emit(66)
            for id_proc in range(results[0].get_nb_cam_available()):
                results[3][id_proc].start()

            #Active wait until one frame as been put to all input_buffer.
            #This prevent freezing the window app by waiting the camera inititalisation in the constructor of Select_zenith_camera_widget.
            for i in range(results[0].get_nb_cam_available()):
                while results[1][i].empty():
                    time.sleep(1)
                self.progress.emit(int(66 + 24 * (i + 1) / results[0].get_nb_cam_available()))
            self.finished.emit(True)

