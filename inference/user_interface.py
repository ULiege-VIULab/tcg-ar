class _MediaMTXProcess:
    """Thin subprocess wrapper with the same .start()/.stop() API as mediamtx-py.
    Used when a pre-downloaded binary already exists in <project>/mediamtx/.
    """
    def __init__(self, binary_path):
        self._path = binary_path
        self._proc = None

    def start(self):
        cwd = os.path.dirname(self._path)
        kwargs = {'creationflags': subprocess.CREATE_NO_WINDOW} if platform.system() == 'Windows' else {}
        self._proc = subprocess.Popen([self._path], cwd=cwd, **kwargs)

    def stop(self):
        if self._proc and self._proc.poll() is None:
            self._proc.terminate()
            try:
                self._proc.wait(timeout=5)
            except subprocess.TimeoutExpired:
                self._proc.kill()


def _make_mediamtx_server():
    """Return a server object (either _MediaMTXProcess or MediaMTX from mediamtx-py).
    Kills any stale mediamtx process before returning so the binary is not locked.
    """
    exe_name = 'mediamtx.exe' if platform.system() == 'Windows' else 'mediamtx'
    local_exe = os.path.join(PROJECT_ROOT, 'mediamtx', exe_name)

    # Kill any leftover mediamtx process from a previous crashed run so the
    # binary file is not locked when mediamtx-py tries to overwrite it.
    if platform.system() == 'Windows':
        subprocess.run(['taskkill', '/f', '/im', exe_name],
                       stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)

    if os.path.isfile(local_exe):
        return _MediaMTXProcess(local_exe)
    return MediaMTX('amd64')


def show_camera_init(detected_cam, progress):
    output_buffer_locks = list()
    output_buffer_wait_events = list()
    output_shared_frame_buffers = list()
    output_processes = list()

    progress.emit(10)

    RSTP_server = _make_mediamtx_server()
    try:
        RSTP_server.start()
    except Exception as e:
        print("Failed to start RTSP server: %s" % e, file=sys.stderr)
        return -1
    
    progress.emit(20)

    for id_cam in detected_cam.get_cam_ids():
        output_buffer_locks.append(Lock())
        output_buffer_wait_events.append(Event())
        shared_frame_buffer = Shared_frame_buffer(id = id_cam,
                                                io = 1,
                                                length = BUFFER_LENGTH,
                                                resolution = (HEIGHT, WIDTH, CHANNELS),
                                                existing_shm = False)
        output_shared_frame_buffers.append(shared_frame_buffer)

    progress.emit(30)

    for id_cam in detected_cam.get_cam_ids():
        id_AR_cam = id_cam + detected_cam.get_nb_open_cam()
        output_buffer_locks.append(Lock())
        output_buffer_wait_events.append(Event())
        shared_frame_buffer = Shared_frame_buffer(id = id_AR_cam,
                                                io = 1,
                                                length = BUFFER_LENGTH,
                                                resolution = (HEIGHT, WIDTH, CHANNELS),
                                                existing_shm = False)
        output_shared_frame_buffers.append(shared_frame_buffer)

    #third output stream per camera: the broadcast "caster" overlay
    for id_cam in detected_cam.get_cam_ids():
        id_caster_cam = id_cam + 2 * detected_cam.get_nb_open_cam()
        output_buffer_locks.append(Lock())
        output_buffer_wait_events.append(Event())
        shared_frame_buffer = Shared_frame_buffer(id = id_caster_cam,
                                                io = 1,
                                                length = BUFFER_LENGTH,
                                                resolution = (HEIGHT, WIDTH, CHANNELS),
                                                existing_shm = False)
        output_shared_frame_buffers.append(shared_frame_buffer)

    for event in output_buffer_wait_events:
        event.set()

    progress.emit(40)

    for id_proc, id_cam in enumerate(detected_cam.get_cam_ids()):
        output_processes.append(Process(target = send_frame_RSTP, name = "Frame writter" + str(id_cam), args = (id_cam, output_buffer_locks[id_proc], output_buffer_wait_events[id_proc])))

    progress.emit(50)

    for id_proc, id_cam in enumerate(detected_cam.get_cam_ids()):
        id_AR_proc = id_proc + detected_cam.get_nb_cam_available()
        id_AR_cam = id_cam + detected_cam.get_nb_open_cam()
        output_processes.append(Process(target = send_frame_RSTP, name = "AR frame writter" + str(id_cam), args = (id_AR_cam, output_buffer_locks[id_AR_proc], output_buffer_wait_events[id_AR_proc])))

    for id_proc, id_cam in enumerate(detected_cam.get_cam_ids()):
        id_caster_proc = id_proc + 2 * detected_cam.get_nb_cam_available()
        id_caster_cam = id_cam + 2 * detected_cam.get_nb_open_cam()
        output_processes.append(Process(target = send_frame_RSTP, name = "Caster frame writter" + str(id_cam), args = (id_caster_cam, output_buffer_locks[id_caster_proc], output_buffer_wait_events[id_caster_proc])))

    progress.emit(60)

    identification_buffer_locks = Lock()
    id_identification_buffer = "id"
    identification_shared_frame_buffer = Shared_frame_buffer(id = id_identification_buffer,
                                                                    io = 1,
                                                                    length = 1,
                                                                    resolution = (HEIGHT, WIDTH, CHANNELS),
                                                                    existing_shm = False)
    renderer = Multi_frame_renderer(number_of_view = detected_cam.get_nb_cam_available())

    progress.emit(70)

    barrier = Barrier(2)
    color_correction = Value(c_bool, False, lock = False)
    new_deck = Value(c_bool, False, lock = False)
    use_fine_tuned = Value(c_bool, SELECTED_USE_FINE_TUNED, lock = False)
    identification_process = Process(target = identification_pipe, name = "Identification pipe", args = (id_identification_buffer, identification_buffer_locks, barrier, color_correction, new_deck, use_fine_tuned))

    for id_proc in range(3 * detected_cam.get_nb_cam_available()):
        output_processes[id_proc].start()

    identification_process.start()
    barrier.wait()

    progress.emit(80)

    #sleep for 1s to insure that the identification process is well terminate if an error occur
    time.sleep(1)

    if not(identification_process.is_alive()):
        RSTP_server.stop()
        for id_proc in range(len(output_processes)):
                output_processes[id_proc].terminate()
        return -1
    else:
        try:
            game_state = Game_state(True)
        except FileNotFoundError:
            RSTP_server.stop()
            for id_proc in range(len(output_processes)):
                output_processes[id_proc].terminate()
            return -1

    progress.emit(90)

    try:
        database_file = open(POKEMON_CARD_DATABASE_FILE, "r", encoding = "utf-8")
        card_database = json.load(database_file)
        database_file.close()
    except FileNotFoundError:
        print("the card database file can not be open", file = sys.stderr)
        return -1
    
    progress.emit(99)

    return [RSTP_server, output_buffer_locks, output_buffer_wait_events, output_shared_frame_buffers, output_processes, identification_buffer_locks, identification_shared_frame_buffer, renderer, identification_process, game_state, color_correction, new_deck, use_fine_tuned, card_database]

class Show_camema_wait_worker(QtCore.QObject):
    progress = QtCore.Signal(int)
    result = QtCore.Signal(list)
    finished = QtCore.Signal(bool)

    def __init__(self, detected_cam):
        super().__init__()
        self.widget = None
        self.detected_cam = detected_cam
        self.results = []

    def run(self):
        self.results = show_camera_init(self.detected_cam, self.progress)
        if self.results == -1:
            self.progress.emit(42)
            self.finished.emit(True)
        else:
            self.result.emit(self.results)
            self.finished.emit(True)

