        frames = list()
        cam_num = 0
        while cam_num < self.detected_cam.get_nb_cam_available():
            frame_read = read_a_frame_from_buffer(buffer = self.input_shared_frame_buffers[cam_num], 
                                                lock = self.input_buffer_locks[cam_num])
            if not(isinstance(frame_read, int)):
                frames.append(frame_read)
                cam_num += 1

        self.button_list = list()

        #non modified frames
        for cam_num, cam_id in enumerate(self.detected_cam.get_cam_ids()):
            frame = frames[cam_num]

            #Resize width and height of a frame image from the same factor being the number of image per column.
            frame_width = np.uint(self.frameSize().width() / self.nb_column)
            frame_height = np.uint(self.frameSize().height() / self.nb_column)
            frame = cv2.resize(frame, (frame_width, frame_height), interpolation = cv2.INTER_AREA)

            self.button_list.append(Picture_button(self, frame, cam_id))
            self.layout.addWidget(self.button_list[cam_num], np.uint(np.floor(cam_num / self.nb_column)) + 1, cam_num % self.nb_column)
            self.button_list[cam_num].clicked.connect(self.button_list[cam_num].press)

